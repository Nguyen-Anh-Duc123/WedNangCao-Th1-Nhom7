    }

    public function add_product_gallery(Request $request, $product_id){
        $this->AuthLogin();
        $files = $request->file('product_gallery');
        if (!$files) {
            Session::put('message','Vui lòng chọn ảnh để tải lên.');
            return Redirect::back();
        }

        $uploadPath = public_path('uploads/product/gallery');
        if (!File::exists($uploadPath)) {
            File::makeDirectory($uploadPath, 0755, true);
        }

        foreach ($files as $file) {
            if ($file && $file->isValid()) {
                $new_image = time().'_'.uniqid().'.'.$file->getClientOriginalExtension();
                $file->move($uploadPath, $new_image);
                DB::table('tbl_product_gallery')->insert([
                    'product_id' => $product_id,
                    'gallery_image' => $new_image,
                    'created_at' => now(),
                    'updated_at' => now(),
                ]);
            }
        }

        Session::put('message','Đã tải lên ảnh chi tiết.');
        return Redirect::back();
    }

    public function delete_product_gallery($gallery_id){
        $this->AuthLogin();
        $gallery = DB::table('tbl_product_gallery')->where('gallery_id', $gallery_id)->first();
        if ($gallery && $gallery->gallery_image) {
            $path = public_path('uploads/product/gallery/'.$gallery->gallery_image);
            if (File::exists($path)) {
                File::delete($path);
            }
        }
        DB::table('tbl_product_gallery')->where('gallery_id', $gallery_id)->delete();
        Session::put('message','Đã xóa ảnh chi tiết.');
        return Redirect::back();
    }
